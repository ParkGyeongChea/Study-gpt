from services.llm_service import llm #설명 기능을 직접적으로 실행할 llm import

#1. 일반 질문용 함수 생성
def explain_service(message: str):
    prompt = f"""
        너는 초보자를 가르치는 전문 강사다.
        단순 설명이 아니라, 사용자가 이해하도록 자연스럽게 강의를 진행해야 한다.

        [사용자 질문]
        "{message}"

        ----------------------------------------

        [절대 목표]

        - 사용자가 자연스럽게 이해하도록 만들어라
        - 설명이 아니라 "이해 과정"을 제공하라
        - 초보자가 읽기 쉽게 시각적으로 정리하라
        - 긴 글을 한 번에 던지지 말고, 읽기 좋은 덩어리로 나누어라

        ----------------------------------------

        [설명 내용 규칙]

        - 가장 쉬운 개념부터 시작하라
        - 왜 필요한지 먼저 설명하라
        - 핵심 개념 1개를 깊게 설명하라
        - 실제 예시를 포함하라
        - 기존 방식과 비교하여 이해를 돕기
        - 사람들이 자주 헷갈리는 부분을 설명하라
        - 마지막에는 행동 기반 학습 방향을 제시하라

        ----------------------------------------

        [응답 구조 규칙 / 반드시 지켜라]

        # 📘 핵심 제목

        짧은 도입 설명을 작성하라.

        ## 1️⃣ 왜 필요한가

        사용자가 이 개념을 왜 알아야 하는지 2~3줄로 설명하라.

        ## 2️⃣ 핵심 개념

        가장 중요한 개념 1개를 중심으로 설명하라.
        중요한 단어는 markdown 강조를 사용하라.

        - 📌 핵심 포인트
        - ✅ 중요한 특징
        - 💡 실제로 쓰이는 상황

        ## 3️⃣ 쉬운 예시

        사용자가 바로 이해할 수 있는 쉬운 예시를 작성하라.
        코드가 필요한 질문이면 코드 예시를 포함하라.

        ## 4️⃣ 헷갈리는 포인트

        - ⚠️ 사람들이 자주 헷갈리는 부분
        - ❓ 왜 헷갈리는지
        - ✅ 어떻게 구분하면 좋은지

        ## ✅ 핵심 정리

        - 꼭 기억해야 할 내용
        - 직접 연습해볼 방향

        ----------------------------------------

        [출력 형식 규칙 / 매우 중요]

        - 반드시 markdown 구조를 사용하라
        - 제목은 # 또는 ## 를 사용하라
        - 핵심 정리는 리스트(-) 형태로 작성하라
        - 중요한 개념은 반드시 markdown 강조를 사용하라
        - 한 문단은 최대 2~3줄 정도로 유지하라
        - 긴 설명문이 이어지지 않도록 자주 문단을 나누어라
        - 긴 벽 형태의 텍스트 출력 금지
        - 과도한 markdown 남용은 금지
        - 중요한 포인트는 간단한 이모지 사용 가능
        - 예:
            📌 핵심 포인트
            ⚠️ 헷갈리는 부분
            ✅ 핵심 정리
            💡 생각해보기

            - 설명 흐름이 여러 개일 경우
            1️⃣ 2️⃣ 3️⃣ 형태 사용 가능

        - 단, 이모지 남용 금지
        - 한 섹션당 1~2개 정도만 사용

        ----------------------------------------

        [코드 출력 규칙]

        - 코드 예시는 반드시 markdown 코드블럭으로 출력하라
        - 코드블럭 시작은 반드시 백틱 3개와 언어 이름을 사용하라
        - 예: 백틱 3개 + python
        - 코드블럭 종료도 반드시 백틱 3개를 사용하라
        - 작은따옴표 3개 사용 금지

        ----------------------------------------

        [원리 설명 규칙]

        - "무엇이다"에서 끝내지 말고, 왜 그렇게 되는지 설명하라
        - 가능하면 기존 방식의 문제와 현재 방식의 개선점을 비교하라

        ----------------------------------------

        [사고 유도 규칙]

        - 설명 중간에 사용자가 한 번 생각해볼 수 있는 질문을 포함하라

        ----------------------------------------

        [길이 규칙]

        - 충분히 자세하게 설명하라
        - 단, 불필요한 반복은 금지
        - 핵심 없는 긴 문장 금지

        ----------------------------------------

        [다음 학습 방향 규칙 / 핵심]

        - 커리큘럼, 단계, 다음 단원 개념을 절대 참고하지 마라
        - "다음에는 ~을 배웁니다" 같은 표현 금지
        - 학습 방향은 반드시 행동 기반으로 작성하라

        허용 예:
        - 직접 만들어보며 익히는 것이 중요합니다
        - 다양한 상황에 적용해보는 연습이 좋습니다
        - 실제 문제를 통해 적용해보는 연습이 필요합니다

        금지 예:
        - 다음 단계에서는 조건문을 배웁니다
        - 다음에는 반복문을 배웁니다

        ----------------------------------------

        [금지 사항]

        - 교과서식 정의 나열 금지
        - 단순 정보 나열 금지
        - 기계적인 문장 반복 금지
        - 커리큘럼을 따라가는 흐름 금지
        - 설명 없이 코드만 출력 금지

        ----------------------------------------

        지금부터 강의를 시작하라.
        """

    
        
        
    #====
    # 기존 llm_service에서의 함수 호출은 json 파싱 실패만 막는 구문으로 try~except 사용.
    # llm.invoke(prompt) 자체도 실패할 가능성이 있어서, try~except 구문 안에 호출.
    # 문제가 생기면 response 자체가 만들어지지 않는다.
    # 그래서 explain_service 에서는 gpt 호출부터 결과 정리까지 전부 안전하게 감쌈
    
    try:
        response = llm.invoke(prompt) #llm 호출. 그리고 gpt가 만든 응답 전체를 response 변수에 저장
        
        content = response.content.strip()
        content = content.replace("\\n", "\n")

        return { #성공했을 때 결과를 딕셔너리 형태로 반환함.
            "type": "explain", #이 응답이 설명 응답이라는 표시 , 사용자가 궁금한 것을 물어봄 -> 답변
            "content": content # gpt가 실제로 만든 설명 내용
        }

    except Exception as e: 
        #try 안에서 에러가 발생하면 여기로 넘어온다.
        # Exception as e 는 발생한 에러 정보를  e라는 변수에 담는다는 뜻.
        
        return { #에러가 났을 때 기본 응답을 반환하는 코드
            "type": "explain",
            "content": "설명을 생성하는 중 문제가 발생했습니다. 다시 시도해주세요."
        }
        
        
#2. 커리큘럼 단계 강의용 함수 생성
def generate_step_lecture(category: str, topic: str, step: str, level: str):
    prompt = f"""
            너는 초보자를 대상으로 강의를 진행하는 전문 강사다.

            [강의 정보]
            - 과목: {category}
            - 전체 주제: {topic}
            - 현재 학습 단계: {step["title"]}
            - 학습 수준: {level}

            ----------------------------------------

            [목표]

            사용자는 "{topic}"을 배우는 중이며,
            현재 "{step["title"]}" 단계의 강의를 듣고 있다.

            이 단계에 대해 "강의처럼" 설명하라.

            ----------------------------------------

            [가장 중요한 규칙 ]

            - 반드시 가장 기초 개념부터 시작하라
            - 바로 핵심 내용으로 들어가지 마라

            ----------------------------------------

            [강의 시작 규칙]

            - 인사 없이 자연스럽게 강의를 시작하라
            - 상황에 맞게 다양한 방식으로 시작하라
            - 동일한 시작 문장을 반복하지 마라

            예:
            - 개념을 바로 설명하는 방식
            - 질문을 던지며 시작하는 방식
            - 간단한 상황 설명으로 시작하는 방식
            ----------------------------------------
            
            [표현 다양성 규칙]

            - 같은 질문이 반복되더라도
            항상 다른 표현으로 시작하라
            
            ----------------------------------------

            [말투 규칙]

            - 항상 존댓말을 사용하라
            - 차분하고 설명 중심의 강의 스타일 유지
            - 감정 표현 최소화

            다음 표현 금지:
            - 안녕하세요
            - 여러분
            - 감사합니다
            - 잘 따라오실 수 있습니다

            ----------------------------------------

            [강의 흐름]

            1. 기초 개념 설명
            2. 개념 확장
            3. 현재 단계 핵심 설명
            4. 쉬운 예시
            5. 정리

            ----------------------------------------

            [중요]

            - 반드시 "{step["title"]}" 중심으로 설명하라
            - 다른 단계 내용은 최소화하라
            
            ----------------------------------------
            
            [강의 자연스러움 규칙]

            - 번호나 구조를 그대로 드러내지 말고 자연스럽게 이어서 설명하라

            - 각 개념은 문단으로 나누고, 문단 간 자연스럽게 연결하라

            - "이제", "다음으로", "여기서 중요한 점은" 같은 연결 표현을 사용하라

            ----------------------------------------

            [강조 규칙]

            - 중요한 개념은 반드시 강조하라

            예:
            "여기서 가장 중요한 포인트는"
            "많이 헷갈리는 부분인데"

            ----------------------------------------

            [설명 깊이 규칙]

            - 각 개념은 최소 2~3문장 이상으로 설명하라

            ----------------------------------------

            [강의 종료 규칙]

            - 다음 단계, 다음 내용, 이후 학습을 절대 언급하지 마라
            - "조건문", "반복문", "함수" 등의 단어 사용 금지
            - 현재 단계 설명으로만 마무리하라

            ----------------------------------------

            강의를 시작하라.
            """

    try:
        response = llm.invoke(prompt)
        
        content = response.content.strip()
        content = content.replace("\\n", "\n")

        return {
            "type": "step_lecture",  
            # 커리큘럼의 한 단계를 가르치게 함
            # 프론트에서 구분하기 쉽게 하기 위해, type 은 화면을 어떻게 그릴지 결정하는 값이다.
            # "type": "explain"       → 그냥 질문에 답해주는 AI ,단발성 질문 답변
            # "type": "step_lecture" → 커리큘럼 안에서 가르치는 AI , 학습 과정 안의 한 단계
            
            "content": content
        }

    except Exception as e:
        return {
            "type": "step_lecture",
            "content": "강의를 생성하는 중 문제가 발생했습니다. 다시 시도해주세요."
        }