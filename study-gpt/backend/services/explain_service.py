# 실제 설명 생성 기능을 담당하는 파일

from services.llm_service import llm #설명 기능을 직접적으로 실행할 llm import


def explain_service(message: str):

    prompt = f"""
            너는 초보자를 대상으로 강의를 진행하는 전문 강사다.

            [사용자 질문]
            "{message}"

            ----------------------------------------

            [목표]

            사용자가 이 주제를 처음 배우는 사람이라고 가정하고,
            "실제 강의처럼" 자연스럽고 친절하게 설명하라.

            단순 설명이 아니라,
            사용자가 이해하고 따라올 수 있도록 "가르치는 것"이 목표다.

            ----------------------------------------

            [가장 중요한 규칙 ⭐]

            - 어떤 주제든지 바로 핵심 개념부터 설명하지 마라

            - 반드시 "기본 개념(기초)"부터 시작하라

            예:
            - 프로그래밍 → 변수, 데이터 타입
            - 영어 → 문장 구조 (주어 + 동사)
            - 수학 → 사칙연산
            - 게임 개발 → 프로그래밍 기초 / 엔진 개념
            - 화성학 → 음계, 장/단 개념

            - 초보자가 이해할 수 있도록
            "가장 쉬운 개념 → 점점 확장" 순서로 설명하라

            ----------------------------------------

            [강의 스타일 규칙]

            - 설명이 아니라 "강의"를 하듯이 말하라

            - 반드시 자연스럽게 사람에게 설명하듯 말하라

            - 아래 표현을 반드시 포함하라:

            "처음 보시면 조금 헷갈릴 수 있는데요"
            "지금부터 하나씩 천천히 이해해보겠습니다"
            "여기서 가장 중요한 포인트는"
            "많이 헷갈리는 부분인데"
            "이렇게 생각하시면 이해가 쉽습니다"

            ----------------------------------------

            [상호작용 규칙]

            - 설명 중간에 이해를 유도하는 흐름을 포함하라

            예:
            "여기까지 이해되셨나요?"
            "이 부분은 꼭 이해하고 넘어가셔야 합니다"
            "지금 개념이 어렵다면 이렇게 생각해보세요"

            ----------------------------------------

            [강의 흐름 규칙]

            - 반드시 아래 흐름으로 설명하라:

            1. 가장 기초 개념
            2. 그 다음 개념으로 확장
            3. 핵심 개념 설명
            4. 전체 연결

            ----------------------------------------

            [강의 구조]

            아래 형식을 따르되,
            제목을 그대로 쓰지 말고 자연스럽게 바꿔라

            1. 기본 개념부터 이해해보기
            2. 왜 이 개념이 필요한가
            3. 어떻게 동작하는가
            4. 쉬운 예시
            5. (조건부) 코드 예제
            6. 실제 활용 방법
            7. 정리

            ----------------------------------------

            [코드 예제 규칙]

            - 프로그래밍 관련일 때만 포함
            - 필요 없으면 항목 자체 출력 금지

            ----------------------------------------

            [학습 유도 규칙 ⭐]

            - 설명이 끝나면 반드시 포함:

            1. 다음에 무엇을 배우면 좋은지
            2. 간단한 실습 제안

            ----------------------------------------

            [출력 규칙]

            - 자연스럽게 이어지는 강의 형태로 작성
            - 딱딱한 교과서 말투 금지
            - "개념 설명" 같은 표현 그대로 쓰지 마라
            - markdown 기호 금지
            - "\\n" 출력 금지
            - 불필요한 인사말 금지

            ----------------------------------------

            강의를 시작하라.
            """
    #====
    # 기존 llm_service에서의 함수 호출은 json 파싱 실패만 막는 구문으로 try~except 사용.
    # llm.invoke(prompt) 자체도 실패할 가능성이 있어서, try~except 구문 안에 호출.
    # 문제가 생기면 response 자체가 만들어지지 않는다.
    # 그래서 explain_service 에서는 gpt 호출부터 결과 정리까지 전부 안전하게 감쌈
    
    try:
        response = llm.invoke(prompt) #llm 호출. 그리고 gpt가 만든 응답 전체를 response 변수에 저장
        
        content = response.content.strip()
        content = content.replace("\\n", "\n")

        return { #성공했을 때 결과를 딕셔너리 형태로 반환함.
            "type": "explain", #이 응답이 설명 응답이라는 표시
            "content": content # gpt가 실제로 만든 설명 내용
        }

    except Exception as e: 
        #try 안에서 에러가 발생하면 여기로 넘어온다.
        # Exception as e 는 발생한 에러 정보를  e라는 변수에 담는다는 뜻.
        
        return { #에러가 났을 때 기본 응답을 반환하는 코드
            "type": "explain",
            "content": "설명을 생성하는 중 문제가 발생했습니다. 다시 시도해주세요."
        }