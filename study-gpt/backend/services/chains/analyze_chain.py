#입력 해석

from langchain_core.prompts import ChatPromptTemplate
from services.shared_llm import llm


analyze_prompt = ChatPromptTemplate.from_template(
        """
        너는 사용자의 학습 요청을 분석하여 구조화된 데이터로 변환하는 AI다.

        [입력]
        "{message}"

        ----------------------------------------

        [목표]
        사용자의 입력을 분석하여 아래 3가지 정보를 추출하라.

        1. category
        - 사용자가 배우고자 하는 큰 분야 또는 과목 이름
        - 예: "파이썬", "자바스크립트", "영상 편집", "랭체인", "플로리스트"

        2. topic
        - 사용자가 실제로 배우고 싶다고 말한 구체적인 학습 주제
        - 사용자가 특정 개념을 말한 경우, 반드시 그 개념을 topic으로 유지하라
        - 사용자의 주제를 과도하게 넓히거나 다른 주제로 바꾸지 마라

        매우 중요:
        - "파이썬 함수"라고 입력하면 topic은 반드시 "파이썬 함수"여야 한다
        - "파이썬 리스트"라고 입력하면 topic은 반드시 "파이썬 리스트"여야 한다
        - "자바스크립트 비동기"라고 입력하면 topic은 반드시 "자바스크립트 비동기"여야 한다
        - "랭체인 LCEL"이라고 입력하면 topic은 반드시 "랭체인 LCEL"이어야 한다

        ----------------------------------------

        [topic 생성 규칙]

        A. 사용자가 특정 개념/기술/문법/기능을 말한 경우:
        - topic은 그 특정 개념을 그대로 반영하라
        - 전체 기초 과정으로 바꾸지 마라

        예:
        - "파이썬 함수에 대해 배우고 싶어" → topic: "파이썬 함수"
        - "파이썬 리스트 배우고 싶어" → topic: "파이썬 리스트"
        - "자바스크립트 promise 배우고 싶어" → topic: "자바스크립트 Promise"
        - "SQL join 배우고 싶어" → topic: "SQL JOIN"

        B. 사용자가 과목만 말하고 특정 개념을 말하지 않은 경우:
        - 해당 과목의 입문 전체 흐름으로 topic을 생성하라

        예:
        - "파이썬 배우고 싶어" → topic: "파이썬 기초 전체"
        - "자바스크립트 배우고 싶어" → topic: "자바스크립트 기초 전체"
        - "영상 편집 배우고 싶어" → topic: "영상 편집 입문 전체 흐름"

        C. 사용자가 "기초", "입문", "처음", "초보", "기본" 같은 표현을 포함한 경우:
        - 특정 개념이 함께 있으면 특정 개념을 유지하라
        - 특정 개념 없이 과목만 있으면 입문 전체 흐름으로 생성하라

        예:
        - "파이썬 함수 기초 배우고 싶어" → topic: "파이썬 함수"
        - "파이썬 리스트 처음 배우고 싶어" → topic: "파이썬 리스트"
        - "파이썬 기초 배우고 싶어" → topic: "파이썬 기초 전체"

        ----------------------------------------

        3. level
        - 반드시 다음 중 하나만 선택:
        "초급", "중급", "고급"

        [level 판단 기준]
        - 초급: 입문, 처음, 기초, 초보, 기본
        - 중급: 실무 수준, 경험 있음, 어느 정도 알고 있음, 중간 수준
        - 고급: 심화, 고급, 전문가, 내부 원리, 아키텍처, 최적화

        사용자가 수준을 명시하지 않으면 기본값은 "초급"이다.

        ----------------------------------------

        [출력 규칙]
        - 반드시 JSON 한 줄만 출력
        - JSON 외 텍스트 절대 금지
        - 설명 금지
        - 코드블록 금지

        ----------------------------------------

        [출력 형식]
        {{"category": "파이썬", "topic": "파이썬 함수", "level": "중급"}}
        """
        )

analyze_chain = analyze_prompt | llm