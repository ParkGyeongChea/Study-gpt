#analyze_chain.py

#입력 해석

from langchain_core.prompts import ChatPromptTemplate
from services.shared_llm import llm


analyze_prompt = ChatPromptTemplate.from_template(
        """
        너는 사용자의 학습 요청을 분석하여 구조화된 데이터로 변환하는 AI다.

        [입력]
        "{message}"

        ----------------------------------------

        [목표]
        사용자의 입력을 분석하여 아래 3가지 정보를 추출하라:

        1. category (과목 이름)
        - 사용자가 배우고자 하는 분야를 그대로 반영
        - 제한 없이 자유롭게 생성
        - 예: "파이썬", "영상 편집", "플로리스트", "게임 개발"

        2. topic (세부 학습 주제)
        - 해당 과목에서 실제로 배우는 "구체적인 학습 대상"이어야 한다
        - 반드시 명확한 개념, 기술, 작업 단위로 작성

        매우 중요:
        - 절대 추상적인 표현을 사용하지 마라

        금지 표현:
        - "공부"
        - "학습"
        - "이해하기"

        ----------------------------------------

        예외 규칙 (핵심 추가)

        - 사용자가 아래와 같은 표현을 포함하면:
        "기본", "기초", "처음", "입문", "시작"

        → 특정 개념 하나가 아니라  
        → 해당 과목의 전체 기초 학습 흐름을 대표하는 topic을 생성하라

        예:
        - "파이썬 기본 배우고 싶어" → "파이썬 기초 전체"
        - "영상편집 처음 배우고 싶어" → "영상 편집 입문 전체 흐름"

        ----------------------------------------

        예시 변환:

        - "플로리스트 배우고 싶어" → "꽃다발 제작"
        - "한문 기초 배우고 싶어" → "기초 한자 학습 흐름"
        - "랭체인 배우고 싶어" → "체인 구조 설계"

        ----------------------------------------

        3. level (학습 수준)
        - 반드시 다음 중 하나만 선택:
        "초급", "중급", "고급"

        ----------------------------------------

        [level 판단 기준]
        - 초급: 입문, 처음, 기초, 초보
        - 중급: 경험 있음, 중간 수준
        - 고급: 심화, 전문가

        ----------------------------------------

        [출력 규칙]
        - 반드시 JSON 한 줄만 출력
        - JSON 외 텍스트 절대 금지
        - 설명 금지
        - 코드블록 금지

        ----------------------------------------

        [출력 형식]
        {{"category": "파이썬", "topic": "파이썬 기초 전체", "level": "초급"}}
        """
        )

analyze_chain = analyze_prompt | llm