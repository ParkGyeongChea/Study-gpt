# curriculum_chain.py

# 커리큘럼 생성 Prompt + LLM 연결 전담 파일


from langchain_core.prompts import ChatPromptTemplate
# ChatPromptTemplate= 랭체인 prompt 템플릿 생성기 , {category} 같은 변수를 Prompt 안에서 사용 가능하게 해줌.
from services.shared_llm import llm

# 1. 커리큘럼 프롬프트
curriculum_prompt = ChatPromptTemplate.from_template(
        """
        너는 초보자를 위한 학습 커리큘럼을 설계하는 AI 튜터다.

        [입력 정보]
        - 과목: {category}
        - 학습 주제: {topic}
        - 학습 수준: {level}
        - 단계 수: {step_count}

        ----------------------------------------

        [목표]
        사용자가 실제로 공부를 시작할 수 있도록,
        {step_count}단계의 학습 커리큘럼을 만들어라.

        각 단계는 단순한 단어가 아니라,
        "무엇을 배우는지"가 분명한 문장으로 작성하라.

        ----------------------------------------

        [중요 규칙]

        1. 절대 아래처럼 출력하지 마라:
        - "{topic} 학습을 위한 단계 1"
        - "{topic} 학습을 위한 단계 2"
        - "1단계"
        - "기초 학습"
        - "개념 이해"

        2. 각 단계는 반드시 실제 학습 내용이어야 한다.

        3. 사용자가 "기초", "기본", "처음", "입문"을 요청한 경우:
        - 해당 과목의 전체 기초 흐름을 단계별로 구성하라.
        - 특정 개념 하나에만 치우치지 마라.

        4. 사용자가 특정 개념을 요청한 경우:
        - 해당 개념을 중심으로 단계별 설명 흐름을 구성하라.

        5. {level} 수준에 맞게 난이도를 조절하라.

        ----------------------------------------

        [좋은 예시]

        입력:
        - 과목: 파이썬
        - 학습 주제: 파이썬 기초 전체
        - 학습 수준: 초급
        - 단계 수: 5

        출력:
        [
        "파이썬이 무엇이고 어떤 분야에서 사용되는지 이해하기",
        "변수와 데이터 타입을 사용해 값을 저장하는 방법 배우기",
        "조건문을 사용해 상황에 따라 코드를 다르게 실행하는 방법 익히기",
        "반복문을 사용해 같은 작업을 여러 번 처리하는 방법 배우기",
        "함수를 만들어 코드를 재사용하는 기본 방법 익히기"
        ]

        ----------------------------------------

        [출력 규칙]
        - 반드시 JSON 배열만 출력하라.
        - 설명 문장 금지
        - 코드블록 금지
        - 번호를 문자열 안에 넣지 마라.
        - 배열의 길이는 반드시 {step_count}개여야 한다.

        출력 형식:
        [
        "첫 번째 학습 단계",
        "두 번째 학습 단계",
        "세 번째 학습 단계"
        ]
        """
)

#출력을 다음 객체로 연결
# Prompt 생성 -> 자동으로 GPT에게 전달
curriculum_chain = curriculum_prompt | llm