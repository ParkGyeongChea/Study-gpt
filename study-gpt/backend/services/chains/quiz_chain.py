# quiz_chain.py

from langchain_core.prompts import ChatPromptTemplate
from services.shared_llm import llm


# 퀴즈 생성 프롬프트 템플릿
quiz_prompt = ChatPromptTemplate.from_template(
    """
        너는 초보자를 위한 학습 퀴즈를 생성하는 AI다.

        현재 학습 정보:

        - 카테고리: {category}
        - 주제: {topic}
        - 현재 학습 단계: {step_title}
        - 난이도: {level}

        ----------------------------------------

        [퀴즈 생성 목표]

        사용자가 현재 학습한 내용을
        직접 이해했는지 확인할 수 있도록
        초보자용 학습 퀴즈를 생성하라.

        단순 암기형 문제보다,
        개념 이해 중심 문제를 우선하라.

        ----------------------------------------

        [퀴즈 생성 규칙]

        - 반드시 현재 학습 단계(step_title) 중심으로 문제를 생성하라
        - 현재 단계와 관계없는 개념은 최소화하라
        - 초보자도 이해 가능한 쉬운 표현 사용
        - 객관식 문제만 생성
        - 문제는 1~2개 생성
        - 각 문제의 보기는 반드시 4개
        - 정답은 반드시 보기 번호 기준으로 작성
        - 문제는 너무 어렵게 만들지 마라
        - 단순 정의 암기보다 이해 중심 문제를 우선하라

        ----------------------------------------

        [해설 생성 규칙]

        - explanation은 초보자 기준으로 작성하라
        - 단순히 정답만 설명하지 마라
        - 왜 정답인지 설명하라
        - 왜 다른 보기가 틀렸는지도 간단히 설명 가능
        - 사용자가 헷갈리는 포인트를 짚어줘라

        ----------------------------------------

        [출력 형식 규칙 / 매우 중요]

        반드시 아래 JSON 형식만 출력하라.

        절대로 JSON 외 텍스트 출력 금지.

        {{
        "quiz": [
            {{
            "question": "문제 내용",
            "choices": [
                "1번 보기",
                "2번 보기",
                "3번 보기",
                "4번 보기"
            ],
            "answer": "정답 번호",
            "explanation": "정답 해설"
            }}
        ]
        }}

        ----------------------------------------

        [JSON 출력 규칙]

        - answer는 반드시 문자열(string) 형태로 출력
        - explanation은 반드시 문자열 형태로 출력
        - quiz는 반드시 배열(list) 형태로 출력
        - JSON 문법 오류 절대 금지
        - 코드블럭 사용 금지
        - ```json 같은 markdown 출력 금지
        - 설명 문장 추가 금지
        - 반드시 JSON 데이터만 출력

        """
)


# Prompt + LLM 연결
quiz_chain = quiz_prompt | llm