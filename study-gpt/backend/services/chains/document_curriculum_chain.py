#document_curriculum_chain.py

# 업로드 문서를 기반으로
# 학습 커리큘럼 생성


from langchain_core.prompts import ChatPromptTemplate
from services.shared_llm import llm


document_curriculum_prompt = ChatPromptTemplate.from_template(
    """
    너는 업로드된 문서를 분석하여,
    초보자용 학습 커리큘럼을 생성하는 AI 교육 전문가다.

    사용자는 현재 업로드한 PDF 문서를
    AI 강의 형태로 학습하려고 한다.

    --------------------------------------------------

    [문서 내용]

    {context}

    --------------------------------------------------

    [목표]

    문서 내용을 기반으로,
    사용자가 실제로 AI 강의를 들으며 학습할 수 있는
    step 기반 커리큘럼을 생성하라.

    중요:
    단순 요약 목차가 아니라,
    실제 온라인 강의 커리큘럼 수준으로 생성해야 한다.

    각 step은:
    - 하나의 핵심 개념
    - 하나의 기술
    - 하나의 동작 원리

    만 집중적으로 다뤄야 한다.
    --------------------------------------------------

    [매우 중요한 규칙]

    - 반드시 학습 순서를 고려하라
    - 쉬운 개념 → 어려운 개념 순서
    - 초보자 기준으로 구성하라

    매우 중요:

    - step은 실제 온라인 강의 목차 수준으로 생성하라
    - 사용자가 바로 강의를 들을 수 있는 수준의 구체적인 step이어야 한다
    - step 하나는 반드시 하나의 핵심 개념만 다뤄야 한다
    - 실제 배우는 개념/기술 단위로 나누어라
    - 너무 큰 범위의 제목 금지
    - 추상적인 chapter 제목 금지

    절대 금지 예시:
    - "자바스크립트 기초"
    - "React 준비"
    - "프론트엔드 개요"
    - "PDF 활용"
    - "문서 저장"

    좋은 예시:
    - "변수 선언과 값 저장 구조"
    - "함수 호출과 반환값 흐름"
    - "배열 반복 처리와 map 함수"
    - "이벤트 발생과 콜백 실행"
    - "생성 결과를 엑셀 파일로 저장하는 구조"
    - "PDF 저장 흐름과 reportlab 사용"
    - "PDF 전처리와 좌표 기반 추출"

    또한 매우 중요:

    - 사용자가 실제로 단계별 강의를 듣는다고 가정하라
    - step 제목만 봐도 무엇을 배우는지 명확해야 한다
    - step 제목은 구체적이어야 한다
    - 실제 강의 커리큘럼처럼 생성하라
    - 책 소개 느낌 금지
    - 개요 정리 느낌 금지
    - 반드시 "강의 목차" 느낌으로 생성하라
    
    - 절대 단원 제목처럼 작성하지 마라
    - 절대 추상적인 챕터 제목 금지
    - 실제로 강의 가능한 개념 단위로 작성하라
    - step 하나는 "하나의 핵심 개념"만 다뤄야 한다
    - 너무 넓은 범위 금지
    - "기초", "입문", "심화" 같은 추상 표현 금지

    좋은 예시:
    - 변수 선언과 메모리 저장 구조
    - 함수 호출과 반환값 흐름
    - 배열 반복과 map 동작 원리
    - 이벤트 발생과 콜백 실행 구조

    나쁜 예시:
    - 자바스크립트 기초
    - 배열 배우기
    - 객체 이해하기
    - 실습 프로젝트

    --------------------------------------------------

    [출력 규칙]

    - 반드시 step 목록만 출력
    - 번호 형태로 출력
    - 설명 금지
    - 코드블록 금지

    --------------------------------------------------

    출력 예시:

    1. 출석 인정 규칙
    2. 결석 처리 기준
    3. 제적 처리 조건
    4. 증명서 발급 절차
    """
)


document_curriculum_chain = (
    document_curriculum_prompt
    | llm
)


# 문서 기반 curriculum 생성 함수
def generate_document_curriculum(context: str):

    response = (
        document_curriculum_chain.invoke({
            "context": context
        })
    )

    return response.content.strip()