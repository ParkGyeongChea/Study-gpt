# 사용자의 의도 분석 AI 객체

from langchain_core.prompts import ChatPromptTemplate
from services.shared_llm import llm


intent_prompt = ChatPromptTemplate.from_template(
        """
        너는 사용자의 메시지를 분석하여 "의도(Intent)"를 분류하는 AI다.

        [입력]
        "{message}"

        ----------------------------------------

        [Intent 종류]

        1. study
        - 학습을 시작하거나, 무엇을 어떻게 배워야 하는지 묻는 경우

        2. explain
        - 특정 개념, 기술, 도구 하나를 설명해달라는 경우

        3. quiz
        - 문제를 요청하는 경우

        4. chat
        - 일반 대화

        ----------------------------------------

        [핵심 판단 기준 ]

        다음 기준을 반드시 따르라:

        ----------------------------------------

        1. "학습 시작" 의도가 있으면 → study

        다음 표현이 포함되면 study로 판단하라:

        - 배우고 싶어
        - 공부하고 싶어
        - 기초부터
        - 어떻게 시작
        - 어떤 순서
        - 커리큘럼
        - 과정

         이 경우,
        "설명해줘"가 함께 있어도 study로 판단하라

        ----------------------------------------

        2. "단일 개념 설명"이면 → explain

        다음 경우 explain:

        - 특정 개념 하나만 설명 요청
        - 학습 흐름 없이 설명만 요구

        예:
        - "파이썬 함수 설명해줘"
        - "DLSS 기능 뭐야"

        ----------------------------------------

        3. quiz

        - 문제 내줘, 퀴즈 요청

        ----------------------------------------

        4. 나머지 → chat

        ----------------------------------------

        [출력 규칙]

        - 반드시 JSON 한 줄만 출력
        - JSON 외 텍스트 절대 금지

        ----------------------------------------

        [출력 형식]

        {{"intent": "study"}}
        """
)

#intent 프롬프트 생성 -> 자동 gpt 호출 구조를 의미.
intent_chain = intent_prompt | llm