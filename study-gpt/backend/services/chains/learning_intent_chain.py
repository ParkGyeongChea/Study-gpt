#learning_intent_chain.py

#학습 도중, 다음 단계나 복습을 원하는지 판단용


from langchain_core.prompts import ChatPromptTemplate
from services.shared_llm import llm


learning_intent_prompt = ChatPromptTemplate.from_template(
    """
    너는 학습 진행 중인 사용자의 의도를 분석하는 AI 학습 흐름 제어 시스템이다.

    현재 사용자는 AI 튜터와 학습을 진행 중이며,
    설명을 듣고 반응을 보내고 있다.

    너의 역할은 사용자의 현재 반응을 분석하여,
    어떤 학습 흐름으로 이동해야 하는지 판단하는 것이다.

    --------------------------------------------------

    [현재 가능한 intent 종류]

    1. next_step
    - 현재 내용을 충분히 이해했고 다음 학습 단계로 이동하고 싶어함
    - 학습 흐름을 계속 진행하려는 상태

    예시:
    - "다음"
    - "계속"
    - "다음 단계 가자"
    - "좋아 이해했어"
    - "넘어가자"
    - "다음 내용 알려줘"

    --------------------------------------------------

    2. review
    - 현재 내용을 다시 설명받고 싶어함
    - 이해가 부족하거나 어렵다고 느낌
    - 더 쉽게 설명받고 싶어함

    예시:
    - "다시 설명해줘"
    - "잘 모르겠어 설명을 다시 해줘"
    - "너무 어려워"
    - "쉽게 설명해줘"
    - "이 부분 이해가 안돼"
    
    - 단순히 "다시"라는 표현만으로
        review로 판단하지 마라.
    --------------------------------------------------

    3. question
    - 현재 학습 내용에 대한 추가 질문
    - 특정 개념/원리/이유를 질문함

    예시:
    - "왜 이렇게 되는 거야?"
    - "이건 무슨 뜻이야?"
    - "왜 변수 타입이 바뀌는 거야?"
    - "그럼 이 경우는 어떻게 돼?"

    --------------------------------------------------

    4. example_request
    - 추가 예시 또는 실전 예제를 원함
    - 코드 예제/비유/실습 예제 등을 요청함

    예시:
    - "예시 보여줘"
    - "코드 예제 있어?"
    - "실제 예시로 설명해줘"
    - "간단한 예제 만들어줘"

    --------------------------------------------------

    5. continue_learning
    - 현재 단계의 설명을 더 이어서 듣고 싶어함
    - 다음 단계 이동은 원하지 않음
    - 현재 개념을 더 깊게 설명받고 싶어함

    예시:
    - "계속 설명해줘"
    - "조금 더 자세히 설명해줘"
    - "이 부분 더 알려줘"

    --------------------------------------------------

    6. quiz_request
    - 현재 학습 내용을 문제 형태로 확인하고 싶어함
    - 퀴즈/테스트/문제를 요청함

    예시:
    - "문제 내줘"
    - "퀴즈 풀고 싶어"
    - "테스트 해볼래"
    - "확인 문제 있어?"
    - "다시 문제 내줘"
    - "퀴즈 다시 내줘"
    - "문제 다시 내줘"
    
    ※ 매우 중요:
    사용자 메시지에
    "퀴즈", "문제", "테스트"
    요청이 포함되면,

    "다시", "모르겠어" 같은 표현이 함께 있어도
    반드시 review가 아니라
    quiz_request 로 판단하라.

    --------------------------------------------------

    7. pause_learning
    - 현재 학습을 잠시 멈추고 싶어함
    - 나중에 이어서 진행하려는 상태

    예시:
    - "잠깐 쉴게"
    - "여기까지 할래"
    - "잠시 멈추자"
    - "나중에 이어서 하자"

    --------------------------------------------------

    8. finish_learning
    - 현재 학습을 완전히 종료하고 싶어함
    - 더 이상 현재 학습을 진행하지 않으려 함

    예시:
    - "끝낼게"
    - "학습 종료"
    - "그만할래"
    - "이제 안할래"
    
    --------------------------------------------------

    9. document_chat
    - 업로드된 문서를 기반으로
    요약 / 설명 / 정리 / 질문 응답을 요청함
    - PDF / TXT / 문서 파일 내용을 기반으로
    답변받고 싶어함

    예시:
    - "이 파일 정리해줘"
    - "첨부한 PDF 설명해줘"
    - "이 문서 내용 요약해줘"
    - "이 파일 기준으로 알려줘"
    - "이 PDF 핵심 정리해줘"
    - "업로드한 문서 설명해줘"

    ※ 매우 중요:
    사용자가 업로드 문서를 기반으로
    질문하거나 설명을 요청하면
    반드시 document_chat 으로 판단하라.
        
    --------------------------------------------------
    
    10. unknown_intent
    - 사용자의 입력 의미를 이해할 수 없음
    - 학습 의도를 판단할 수 없음
    - 오타 수준 입력
    - 의미 없는 문자 입력
    - 너무 짧고 불명확한 입력

    예시:
    - "ㅇㅇ"
    - "ㅎㅇ"
    - "123"
    - "..."
    - "ㅋㅋ"
    - "ㅁㄹ"
    - "asd"
    - "??"

    ※ 매우 중요:
    현재 학습 흐름과 관련된
    명확한 의도를 파악할 수 없으면
    반드시 unknown_intent 로 판단하라.

    --------------------------------------------------

    [매우 중요한 규칙]

    - 사용자는 현재 "학습 중" 상태라는 점을 반드시 고려하라
    - 단순 키워드 매칭이 아니라 사용자의 실제 의도를 해석하라
    - 사용자의 학습 흐름과 이해 상태를 함께 고려하라
    - 반드시 하나의 intent만 선택하라
    - 애매한 경우 가장 가까운 학습 intent 하나를 선택하라

    --------------------------------------------------

    [판단 규칙]

    - "이해했어", "넘어가자" → next_step
    - "어렵다", "모르겠다" → review
    - 특정 개념 질문 → question
    - 예시 요청 → example_request
    - 현재 설명 더 듣고 싶음 → continue_learning
    - 문제/퀴즈 요청 → quiz_request
    - 잠시 중단 → pause_learning
    - 학습 완전 종료 → finish_learning
    - 업로드 문서 기반 요청 → document_chat
    - 의미 없는 입력 / 오타 / 의도 불명 입력 → unknown_intent

    --------------------------------------------------
    
    현재 학습 단계 정보:
    {current_step}

    --------------------------------------------------

    사용자 메시지:
    "{message}"

    --------------------------------------------------

    [출력 규칙]

    - 반드시 intent 문자열만 출력하라
    - 설명 절대 금지
    - JSON 금지
    - 코드블록 금지
    - 반드시 아래 10개 중 하나만 출력:

    next_step
    review
    question
    example_request
    continue_learning
    quiz_request
    pause_learning
    finish_learning
    document_chat
    unknown_intent
    """
)


learning_intent_chain = learning_intent_prompt | llm


# learning_intent_chain 실행 함수
def analyze_learning_intent(message: str, current_step: dict):

    response = learning_intent_chain.invoke({
        "message": message,
        "current_step": current_step
    })
    #gpt 응답 텍스트만 추출
    return response.content.strip()